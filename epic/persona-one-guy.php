<p><strong>Goals:</strong> The goal of this app for Guy is finally have friends who attend his events so he can actually have fun, spontaneous social life that he has always wanted.</p>

<p><strong>User Story: </strong>Guy Cleve is a man with few identifiable characteristics, few friends, a low  attention span, and is completely foreign to social cues. He is a lonely individual with a sticky note description of a love life, yet, he still refuses to give up his search for the "one". Though his skillset is so minor, he is still described as a tenacious person when put into a situation he is comfortable enough to deal with.  He takes an interest in music, though he does not play any, and aspires to be a well known musician once he figures out which instrument that suites his needs. Guy has been looking for a way to expand his friend base with people well educated on the classical music genre.  Even though unsuccessful in finding a way to do that, he still has hope he will come across a site that will allow him to invite a group of people he can be around and relate to. He wants to be able to check the site, post an event that reaches people within a 5 mile radius, and have people be able to contact him and let him know they will be at that event. He would also like be able to see how well the event is going, how many people are there, and if the event is even worth continuing. If there is a cost of admission, he would like the site to notify his attendees along with how much it will be, and how much will they need to bring to enjoy his their time, and/or buy products supplied at the establishment. Guy does not like to buy into popular opinion, but, he would like a way to see other peoples feed back on the host of the meet-up, and the area or establishment in which the meet-up took place. Hopefully he can find a site where all of his needs can be met, and where he can make himself more appetizing and available to the community around him.
</p>
