		<h1>Usage Case:</h1>
		<h4>Author: Matt David</h4>
		<h2>Event Creator</h2>
		<p>Its Friday night and Guy is excited about kicking off the weekend. Guy likes being the coolest dude that throws the hottest events and wants people to talk about it. He uses crowdvibe to post his event so he can get the word out to his friends and the public. After accepting the invitation, attendees will be able to live rate his event. Guys goal is to be a "10" whose events reach "on fire" every time.</p>
		<h2>Usage preconditions-</h2>
		<p>Guy has created a profile with a user name, email, phone, and rating status. He has also "friended" his close chums. He has downloaded the app.</p>
		<h2>Usage postconditions</h2>
		<p>Guy has created an event that was attended and rated by those who checked in.</p>
		<h2>Interaction flow</h2>
		<ol>
			<li>Guy clicks on his browser icon</li>
			<li>browser returns home screen</li>
			<li>Guy clicks the create event button</li>
			<li>browser returns input form</li>
			<li>Guy enters credentials for even (date/time/location/description/public or friends) and hits submit</li>
			<li>browser returns event created!</li>
			<li>if event is set to public, event will be posted to event board.</li>
			<li>Friends will be sent notification any time an even is posted by a friend.</li>
		</ol>
		<h2>Attendee</h2>
		<p>Devin is pumped its Friday! He has been under the gun at work all week. He hasn't talked to any of the boys since last weeks schinanigans. He gets home later than most and opens up crowdvibe to see whats happening tonight. Devin doesn't want to gamble on his only night out so he want to check out the status of the options in front of him</p>
		<h2>Usage preconditions</h2>
		<p>Devin has downloaded the app. he has set up his profile and added friends</p>
		<h2>Usage postconditions</h2>
		<p>Devin has selected an event, rsvp, checked in, and rated the event</p>
		<h2>Interaction Flow</h2>
		<ol>
			<li>Devin opens the browser</li>
			<li>browser returns home page with tonight's options to browse</li>
			<li>Devin scrolls through events until he finds what he likes.</li>
			<li>Devin clicks rsvp</li>
			<li>browser returns are you sure?</li>
			<li>Devin confirms his rsvp</li>
			<li>app confirms his rsvp and places the even on his itinerary</li>
			<li>Devin finishes his beer, gets the three s's out of the way. gets dressed and makes his way to the event. He opens his app when he gets there, and clicks the event on his itinerary.</li>
			<li>browser returns have you arrived?</li>
			<li>Devin clicks to check in.</li>
			<li>browser returns event page with rating input options</li>
			<li>Devin gets 5 beers in and decides this event is awesome</li>
			<li>Devin rates the event an 8 and clicks submit</li>
			<li>browser confirms his rating</li>
			<li>Devin gets 8 beers in and decides he has beef with the event host. He clicks on the creator profile</li>
			<li>browser returns creators profile</li>
			<li>Devin rates him a 5 because he grew a little drunken compassion.</li>
			<li>browser confirms rating</li>
			<li>Devin gets the spins, passes out in a chair, and wakes up in his clothes on his couch with his own rating down 3 points.
			</li>
		</ol>
